<div class="blabla help text-left">
The JDPT is a collection of the most used kanji in Japanese drama subtitle files, sorted in 6 levels to imitate the JLPT and other vocabulary lists you find on the web. 
<br />
It's a list I made for fun, 
although it is quite interesting to see what words are the most common in dramas compared to other corpus such as newspaper or the Japanese Wikipedia.

<br />
The kanji are assigned to one of 6 JDPT levels according to the cumulative frequency of the kanji. 
For example, all JDPT 5 kanji together represent 50% of all kanji used accross all dramas while all JDPT 5 and 4 together represent 75% of all kanji used.
The exact repartition is as follows:
