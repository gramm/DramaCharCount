<div class="blabla help text-left">
For each kanji, the following information is displayed:<br />
</div>

<table class="table table-sm text-left"><tr>
<tr><td>Kanji</td><td>The kanji itself :o)</td></tr>
<tr><td>Count</td><td>The number of occurrences between all subtitle files.</td></tr>
<tr><td>Frequency</td><td>The occurrence frequency i.e. count for this kanji divided by the total kanji count (which does not include hiragana for example).</td></tr>
<tr><td>Cumulated Frequency</td><td>The cumulative frequency. For example a value of 50% means that the number of occurrences of all kanji up to the current kanji represent 50% of every kanji in every dramas.</td></tr>
<tr><td>Drama Frequency</td><td>The frequency of appearance of this kanji across dramas. For example a value of 30% means that this kanji appears in 30% of all dramas (and never appears once in the other 70% of dramas).</td></tr>
<tr><td>Episode Frequency</td><td>The frequency of appearance of this kanji across episodes. For example a value of 30% means that this kanji appears in 30% of all episodes (and never appears once in the other 70% of episodes).</td></tr>
<tr><td>JDPT</td><td>The JDPT level for this kanji. The JDPT is defined using the cumulative frequency as follows:</td></tr>
<tr><td>JDPT Pos</td><td>The JDPT position i.e. the position when sorted by JDPT level then by count within each level.</td></tr>
<tr><td>Jōyō</td><td>The Jōyō level.</td></tr>
<tr><td>Jōyō Pos</td><td>The Jōyō position i.e. the position when sorted by Jōyō level then by count within each level.</td></tr>
</table>

									



